#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	to_uppercase(char *str)
{
	int		i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 'a' && str[i] <= 'z')
			ft_putchar(str[i] - 32);
		else
			ft_putchar(str[i]);
		i++;
	}
}

int		main(int size, char **str)
{
	if (size == 2)
		to_uppercase(str[1]);
	ft_putchar('\n');
	return (0);
}
